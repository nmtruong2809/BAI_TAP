CREATE TABLE USERS (
    UID_ NUMBER PRIMARY KEY,
    USERNAME VARCHAR2(100),
    PASS VARCHAR2(100),
    REGDAY DATE DEFAULT SYSDATE, 
    NATIONALITY VARCHAR2(50)
);


CREATE TABLE CHANNEL (
    CHANNELID VARCHAR2(10) PRIMARY KEY,
    CNAME VARCHAR2(100),
    SUBSCRIBES NUMBER,
    OWNER NUMBER,
    CREATED DATE,
    CONSTRAINT FK_CHANNEL_USER FOREIGN KEY (OWNER) REFERENCES USERS(UID_)
);


CREATE TABLE VIDEO (
    VIDEOID VARCHAR2(10) PRIMARY KEY,
    TITLE VARCHAR2(200),
    DURATION NUMBER,
    AGE NUMBER
);


CREATE TABLE SHARE (
    VIDEOID VARCHAR2(10),
    CHANNELID VARCHAR2(10),
    PRIMARY KEY (VIDEOID, CHANNELID),
    CONSTRAINT FK_SHARE_VIDEO FOREIGN KEY (VIDEOID) REFERENCES VIDEO(VIDEOID),
    CONSTRAINT FK_SHARE_CHANNEL FOREIGN KEY (CHANNELID) REFERENCES CHANNEL(CHANNELID)
);

