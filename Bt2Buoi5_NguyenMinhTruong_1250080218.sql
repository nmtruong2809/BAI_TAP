CREATE OR REPLACE TRIGGER TRG_CHECK_DATE
BEFORE INSERT OR UPDATE ON CHANNEL
FOR EACH ROW
DECLARE
    V_REGDAY DATE;
BEGIN
    SELECT REGDAY INTO V_REGDAY FROM USERS WHERE UID_ = :NEW.OWNER;
    IF :NEW.CREATED < V_REGDAY THEN
        RAISE_APPLICATION_ERROR(-20001, 'Ngày tạo kênh phải >= ngày đăng ký người dùng.');
    END IF;
END;
