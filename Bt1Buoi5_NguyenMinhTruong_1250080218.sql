CREATE TABLE TUYEN (
    MATUYEN VARCHAR2(10) PRIMARY KEY,
    BENDAU VARCHAR2(50),
    BENCUOI VARCHAR2(50),
    GIATUYEN NUMBER,
    NGXB DATE,
    TGDK NUMBER
);


CREATE TABLE XE (
    MAXE VARCHAR2(10) PRIMARY KEY,
    BIENKS VARCHAR2(20),
    MATUYEN VARCHAR2(10),
    SOGHET1 NUMBER,
    SOGHET2 NUMBER,
    CONSTRAINT FK_XE_TUYEN FOREIGN KEY (MATUYEN) REFERENCES TUYEN(MATUYEN)
);


CREATE TABLE KHACH (
    MAHK VARCHAR2(10) PRIMARY KEY,
    HOTEN VARCHAR2(100),
    GIOITINH VARCHAR2(10),
    CMND VARCHAR2(20)
);

CREATE TABLE VEXE (
    MATUYEN VARCHAR2(10),
    MAHK VARCHAR2(10),
    NGMUA DATE,
    GIAVE NUMBER,
    PRIMARY KEY (MATUYEN, MAHK, NGMUA),
    CONSTRAINT FK_VEXE_TUYEN FOREIGN KEY (MATUYEN) REFERENCES TUYEN(MATUYEN),
    CONSTRAINT FK_VEXE_KHACH FOREIGN KEY (MAHK) REFERENCES KHACH(MAHK)
);

