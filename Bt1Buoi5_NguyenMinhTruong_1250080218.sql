CREATE OR REPLACE TRIGGER TRG_GIAVE_TET
BEFORE INSERT OR UPDATE ON TUYEN
FOR EACH ROW
BEGIN
    IF :NEW.NGXB BETWEEN TO_DATE('29/12/2016','DD/MM/YYYY') AND TO_DATE('05/01/2017','DD/MM/YYYY') THEN
        :NEW.GIATUYEN := :NEW.GIATUYEN * 1.2;
    END IF;
END;
